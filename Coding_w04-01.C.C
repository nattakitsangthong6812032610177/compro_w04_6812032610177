#include <stdio.h> // Include the standard input/output library for printf()

int main() { // main() function: entry point of the program

    // Print the first line of output
    printf("Hello,\n");     // Output: Hello,
    
    // Print the second line of output
    printf("C Tokens!\n");  // Output: C Tokens!

    return 0; // Return 0 to indicate successful program termination
}
//โปรแกรมแสดงข้อความ "Hello,\nC Tokens!"

//รู้จักประเภท Token เช่น Keyword (int, return), Identifier (main, printf), Constant ("Hello,", 0), Operator (;, \n)

//เข้าใจโครงสร้างพื้นฐานของภาษา C